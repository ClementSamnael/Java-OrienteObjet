package exoCours;

public class Point {

	// Un point est définie par ses coordonnées x et y
	protected int x, y;

	// -------Constructeur POINT-------------------------/

	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}
	// -------GETTER-------------------------/

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

}
